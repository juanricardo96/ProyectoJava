    private void llenarTablaPersonas(){
        String[] nombCols = new String[6];
        nombCols[0] = "Identificaci√≥n";
        nombCols[1] = "Nombres";
        nombCols[2] = "Apellidos";
        nombCols[3] = "Edad";
        nombCols[4] = "Tipo Vacuna";
        nombCols[5] = "Dosis";
        
        ManejadorPersonas mp = new ManejadorPersonas();
        ArrayList<Vacuna> ListaVacunas = mp.obtenerListaFecha(txtConsultaFecha.getText());
        
        String[][] cuerpoTabla = new String[ListaVacunas.size()][6];
        int contador = 0;
        
        for(Vacuna vacuna : ListaVacunas){
            cuerpoTabla[contador][0] = vacuna.getNum_documento();
            cuerpoTabla[contador][1] = vacuna.getNombres();
            cuerpoTabla[contador][2] = vacuna.getApellidos();
            cuerpoTabla[contador][3] = vacuna.getEdad() + "";
            cuerpoTabla[contador][4] = vacuna.getTipoVacuna();
            cuerpoTabla[contador][5] = vacuna.getTipoDosis();
            
            contador++;
        }
        DefaultTableModel modeloVacunas = new DefaultTableModel(cuerpoTabla, nombCols);
        tblConsulta.setModel(modeloVacunas);
    }

