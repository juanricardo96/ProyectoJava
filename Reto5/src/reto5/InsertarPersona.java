    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (validarCampos()){
            ManejadorPersonas mp = new ManejadorPersonas();
            
            TipoVacuna tv = (TipoVacuna)cmbTipoVacuna.getSelectedItem();
            TipoDosis td = (TipoDosis)cmbTipoDosis.getSelectedItem();
            
            Persona persona = new Persona(txtNumDoc.getText(), txtNombre1.getText(), txtNombre2.getText(), txtApellido1.getText(), 
                    txtApellido2.getText(), Double.parseDouble(txtEdad.getText()), txtTelefono.getText());
            Vacuna vacuna = new Vacuna(tv.getIdTipoVacuna(), td.getIdTipoDosis());
            boolean resultado = mp.crearPersona(persona);
            boolean resultado2 = mp.crearVacuna(vacuna, persona);
            if(resultado && resultado2){
                JOptionPane.showMessageDialog(this, "Persona registrada con éxito", "Registro de Personas", JOptionPane.INFORMATION_MESSAGE);
                limpiarFormulario();
            } else {
                JOptionPane.showMessageDialog(this, "Error en la base de datos al registrar la persona", "Error en los datos", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

