    private boolean validarCampos(){
        if (txtNumDoc.getText().length() < 6){
            JOptionPane.showMessageDialog(this, "El numero de documento no es valido", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (txtNombre1.getText().equals("")){
            JOptionPane.showMessageDialog(this, "El nombre de la persona no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (txtApellido1.getText().equals("")){
            JOptionPane.showMessageDialog(this, "El apellido de la persona no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (txtEdad.getText().equals("")){
            JOptionPane.showMessageDialog(this, "La edad de la persona no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }else {
            try {
                double edad = Double.parseDouble(txtEdad.getText());
                if(edad < 0){
                    JOptionPane.showMessageDialog(this, "La edad debe ser mayor a 0", "Error", JOptionPane.ERROR_MESSAGE);
                    return false;  
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "La edad debe ser un numero", "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        if (txtTelefono.getText().equals("")){
            JOptionPane.showMessageDialog(this, "El teléfono de la persona no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } 
        if (cmbTipoVacuna.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de vacuna", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (cmbTipoDosis.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de dosis", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private void limpiarFormulario() {
        txtNumDoc.setText("");
        txtNombre1.setText("");
        txtNombre2.setText("");
        txtApellido1.setText("");
        txtApellido2.setText("");
        txtEdad.setText("");
        txtTelefono.setText("");
        cmbTipoVacuna.setSelectedIndex(-1);
        cmbTipoDosis.setSelectedIndex(-1);
    }
    
